-- Check actual parents of COWL_SILL_SIDE children
SET LINESIZE 200

SELECT
    p.OBJECT_ID as CHILD_ID,
    p.NAME_S_ as CHILD_NAME,
    r.FORWARD_OBJECT_ID as PARENT_ID
FROM DESIGN12.PART_ p
INNER JOIN DESIGN12.REL_COMMON r ON p.OBJECT_ID = r.OBJECT_ID
WHERE p.OBJECT_ID IN (18208702, 18208714, 18208725, 18208734)
ORDER BY p.OBJECT_ID, r.FORWARD_OBJECT_ID;

EXIT
