-- Sample: Get collection with class name
SELECT c.OBJECT_ID, c.CAPTION_S_, c.CLASS_ID, cd.NAME as CLASS_NAME, cd.NICE_NAME
FROM DESIGN1.COLLECTION_ c
LEFT JOIN DESIGN1.CLASS_DEFINITIONS cd ON c.CLASS_ID = cd.TYPE_ID
WHERE ROWNUM <= 10;
