-- Complete data dump of all Level 1 children with ALL fields
-- to help identify what field contains the Siemens order

COLUMN CAPTION FORMAT A30
COLUMN CLASS_TYPE FORMAT A25
COLUMN STATUS FORMAT A10
COLUMN EXTERNALID FORMAT A50

SELECT
    '=== NODE DATA ===' AS INFO,
    c.CAPTION_S_ AS CAPTION,
    c.OBJECT_ID,
    c.OBJECT_VERSION_ID AS C_OBJ_VER_ID,
    c.CLASS_ID AS C_CLASS_ID,
    cd.NICE_NAME AS CLASS_TYPE,
    c.STATUS_S_ AS STATUS,
    c.MODIFIED,
    c.SOFT_FIELDS_EXIST,
    c.CHILDREN_VR_,
    c.COLLECTIONS_VR_,
    TO_CHAR(c.MODIFICATIONDATE_DA_, 'YYYY-MM-DD HH24:MI') AS MOD_DATE,
    c.LASTMODIFIEDBY_S_ AS LAST_MOD_BY,
    c.CREATEDBY_S_ AS CREATED_BY,
    '=== REL DATA ===' AS REL_INFO,
    r.OBJECT_VERSION_ID AS R_OBJ_VER_ID,
    r.CLASS_ID AS R_CLASS_ID,
    r.SEQ_NUMBER,
    r.REL_TYPE,
    r.FIELD_NAME,
    r.FORWARD_OBJECT_ID AS PARENT_ID,
    ROWIDTOCHAR(r.ROWID) AS REL_ROWID
FROM DESIGN12.REL_COMMON r
LEFT JOIN DESIGN12.COLLECTION_ c ON r.OBJECT_ID = c.OBJECT_ID
LEFT JOIN DESIGN12.CLASS_DEFINITIONS cd ON c.CLASS_ID = cd.TYPE_ID
WHERE r.FORWARD_OBJECT_ID = 18140190
ORDER BY r.OBJECT_VERSION_ID;

EXIT;
